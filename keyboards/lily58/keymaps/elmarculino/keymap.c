bool get_ignore_mod_tap_interrupt(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LCTL_A:
    case LALT_S:
    case LSFT_D:
    case LGUI_F:
    case RGUI_J:
    case RSFT_K:
    case RALT_L:
    case RCTL_SC:
      return true;
    default:
      return false;
  }
}

